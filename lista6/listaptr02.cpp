int i = 99, j; // Declara duas variáveis inteiras, 'i' e 'j', e 'i', recebe 11
int *p; // Declara um ponteiro do tipo inteiro chamado 'p'
p = &i; // O ponteiro 'p' recebe o endereço de memória da variável i
j = *p + 100; // A variável 'j' recebe o valor do ponteiro que contém o endereço de memória de 'i' mais 100