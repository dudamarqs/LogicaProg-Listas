int a = 5, b = 12; //Declara duas variáveis inteiras, onde 'a' recebe 5 e 'b' recebe 12
int *p; // Declara uma variável do tipo ponteiro chamada 'p'
int *q; // Declara outra variável do tipo ponteiro chamada 'q'
p = &a; // O ponteiro 'p' recebe o endereço de memória de 'a'
q = &b; // O ponteiro 'q' recebe o endereço de memória de 'b'
int c = *p + *q; // Declara uma variável do tipo inteira, onde 'c' recebe o valor da soma dos valores contidos dentro dos ponteiros 
